#Tipo de dato entero o largo long
numero = 15
#con una coma en print pueden colocarse varios valores
print(numero, type(numero))


#Tipo de dato flotante
num_flotante = 15.5
print(num_flotante, type(num_flotante))

#TIpo de dato complejo
num_complejo = 5 + 6j
print(num_complejo, type(num_complejo))

#Tipo de dato String
nombre = "Cecy"
print(nombre, type(nombre))

#Tipo de dato booleano
verdadero_falso = 3 == 3 #Dos signos de igual es comparaci√≥n
print(verdadero_falso, type(verdadero_falso))
